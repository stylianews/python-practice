split_string = "This string has been\nsplit over\nseveral\nlines"
# \n is to start a new line.
print(split_string)

# \t is for tabs (8 characters usually)
tabbed_string = "1\t2\t3\t4\t5"
print(tabbed_string)

print('The pet shop owner "No, No, \'e\'s uh,...he\'s resting".')
#or
print("The pet shop owner said\"No, no , 'e's uh,... he's resting\".")
#We can also triple quote

another_split = """hehehjsa \
saidjaisjdas \
siajdiasjd
asdjaisdj
asidjasod"""

print(another_split)


